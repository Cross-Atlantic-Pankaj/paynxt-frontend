// Function to get the mapped icon name or fallback to the original
export function getIconName(iconName) {
  if (!iconName) return 'Circle';
  
  // Try exact match first
  if (iconMapping[iconName]) {
    return iconMapping[iconName];
  }
  
  // Try case-insensitive match
  const lowerIconName = iconName.toLowerCase();
  for (const [key, value] of Object.entries(iconMapping)) {
    if (key.toLowerCase() === lowerIconName) {
      return value;
    }
  }
  
  // Return original if no match found
  return iconName;
}
